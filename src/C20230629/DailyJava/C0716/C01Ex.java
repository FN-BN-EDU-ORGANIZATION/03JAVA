//암호 알고리즘 
//개인키 암호화 : 동일한 키로 암호화하고 복호화하는 암호화 기법. 속도는 빠르지만 관리해야 할 키의 수가 많음
//	- 스트림 암호화 방식 : 평문과 동일한 길이의 스트림을 생성하여 비트 단위로 암호화하는 방법. RC4,TKIP,LFSR
//	- 블록 암호화 방식 : 한번에 하나의 데이터 블록을 암호화하는 방식. DES, SEED, 
//공개키 암호화 : 암호화할 때 사용하는 공개키는 사용자에게 공개하고 복호화할때 비밀키는 관리자가 비밀리에 관리하는 암호화 기법(비대칭 암호 기법)
//
//양방향 알고리즘의 종류
//- SEED : 1999년 한국인터넷진흥원에서 개발. 블록 크기는 128비트이며 키 길이에 따라 128,256. 블록 암호화 알고르짐
//- ARIA : 2004년 국가정보원과 산학연협회가 개발. 블록 암호화 알고리즘
//- DES(Data Encryption Standard) : 1975년 미국 NBS에서 발표한 개인키 암호화 알고리즘. 
//- AES(Advanced Encryption Standard) : 2001년 미국 표준 기술 연구소(NIST)에서 발표한 개인키 암호화 알고리즘. DES의 한계를 NIST에서 공모한 후 발표
//- RSA(Rivest Shamir Adleman) : 1978년 MIT의 라이베스트, 샤미르,애들먼에 의해 제안된 공개키 암호화 알고리즘. 큰 숫자를 소운시분해 하기 어렵다는 것에 기반하여 만들어짐
//- IDEA : 스위스의 라이와 메시가 1990년에 개발한 알고리즘. 블록 크기는 64비트이고 키 길이는 128비트
//- Skipjack : 국가 안전 보장국(NSA)에서 개발한 암호화 알고리즘. 클리퍼 칩(Clipper Chip)이라는 IC칩에 내장되어 있음
//- TKIP : 무선랜 보안에 사용된 WEP을 보완한 데이터 보안 프로토콜. 임시 키 무결성 프로토콜이라고도 함. 
//
