//Secure SDLC
//: SDLC(소프트웨어 개발 생명주기)에 보안 강화를 위한 프로세스를 포함한 것
//대표적인 방법론
//- CLASP : SDLC의 초기 단계에서 보안을 강화하기 위해 개발된 방법론
//- SDL : 마이크로소프트 사에서 안전한 소프트웨어 개발을 위해 기존의 SDLC를 개선한 방법론
//- Seven Touchpoints : 소프트웨어 보안의 모범사례를 SDLC에 통합한 방법론
//
//소프트웨어 개발 보안 요소
//- 기밀성(Confidentiality) : 시스탬 내의 정보와 자원은 인가된 사용자에게만 접근이 허용됨. 정보가 전송 중에 노출되더라도 데이터를 읽을 수 없음
//- 무결성(Integrity) : 시스템 내의 정보는 오직 인가된 사용자만 수정할 수 있음
//- 가용성(Availability) : 인가받은 사용자는 시스템 내의 정보와 자원을 언제라도 사용할 수 있음
//- 인증(Authentication) : 합법적인 사용자인지를 확인하는 모든 행위
//- 부인방지(NonRepudiation) : 데이ㅓ를 송수신한 자가 송수신 사실을 부인할 수 없도록 송수신 증거를 제공
//
//입력 데이터 검증 및 표현
//- SQL 삽입(Injection) : 웹 응요 프로그램에 SQL을 삽입하여 내부 데이터베이스 서버의 데이터를 유출 및 변조하고, 관리자 인증을 우회하는 보안 약점
//- 크로스사이트 스크립팅(XSS) : 웹페이지에 악의적인 스크립트를 삽입하여 방문자들의 정보를 탈취하거나 비정상적인 기능 수행을 유발하는 보안 약점
//- 메모리 버퍼 오버플로 : 연속된 메모리 공간을 사용하는 프로그램에서 할당된 메모리의 범위를 넘어선 위치에서 자료를 읽거나 쓰려고 할 때 발생하는 보안 약점
//
